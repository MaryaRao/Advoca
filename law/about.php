    <p>Advoca is the concept of Hafiz Mohammed Ali Siddiqui, who firmly believes that legal services should be within reach of all classes of society. The only way for this ideal to come to fruition was to create a law firm that changes the perception of lawyers and of the field of law. There is a common misconception in society that lawyers or legal services are only required when something terribly goes wrong, or are reserved for the very few affluent and elite, or are just utterly unapproachable. Advoca aims to change this common narrative in the following ways:</p>

    <li>Advoca is a legal service provider that holds fast to the idea of demystifying and simplifying legal red tape for our clients. We believe in approachability and clarity, and so we say it how it is. We don’t waste the time with our clients while speaking in verbose legal jargon. We are direct about what you should expect from us and what you should reasonably expect as a legal remedy for your case. Then we leave it up to you if you wish to hire our services. We like to be transparent.</li>

    <li>We find that often lawyers and legal firms don’t provide great customer service. We want to change this impression about lawyers, and so we pride ourselves in our direct and frequent correspondence with you. We allow multiple queries throughout your proceeding/service and keep you in the loop about what is happening in your legal matter. We try to build a strong rapport with you so that you feel comfortable approaching us. We literally are a phone call away.</li>

    <li>We want the best result for you. In straightforward cases and legal queries, any law firm or lawyer can help you achieve a solution. We can too. But in cases that are unprecedented, tricky, novel, and simply complicated, we truly specialize. We believe fact-finding is the most crucial step to giving you the best possible advice and service, and so we take extra time to learn more about your business/problem. We work from unique angles to get to the heart of your facts. Only once your legal matter becomes our matter, we can truly proceed with the legal aspect of our service.</li>

